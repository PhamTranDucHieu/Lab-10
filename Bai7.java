    public Bai7() {
        initComponents();
        setTitle("Form phản hồi người dùng");
        setSize(450, 300);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setLocationRelativeTo(null);

        // ===== Panel tiêu đề =====
        JPanel pnlTieuDe = new JPanel(new BorderLayout(5, 5));
        pnlTieuDe.add(new JLabel("Tiêu đề:"), BorderLayout.WEST);
        txtTieuDe = new JTextField();
        pnlTieuDe.add(txtTieuDe, BorderLayout.CENTER);

        // ===== TextArea nội dung =====
        txtNoiDung = new JTextArea(8, 30);
        JScrollPane scrollPane = new JScrollPane(txtNoiDung);

        // ===== Button =====
        btnGui = new JButton("Gửi");

        // ===== Layout chính =====
        setLayout(new BorderLayout(10, 10));
        add(pnlTieuDe, BorderLayout.NORTH);
        add(scrollPane, BorderLayout.CENTER);
        add(btnGui, BorderLayout.SOUTH);

        // ===== Xử lý sự kiện =====
        btnGui.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String noiDung = txtNoiDung.getText().trim();

                if (noiDung.isEmpty()) {
                    JOptionPane.showMessageDialog(
                            null,
                            "Nội dung phản hồi không được để trống!",
                            "Lỗi",
                            JOptionPane.ERROR_MESSAGE
                    );
                } else {
                    JOptionPane.showMessageDialog(
                            null,
                            "Gửi phản hồi thành công!",
                            "Thông báo",
                            JOptionPane.INFORMATION_MESSAGE
                    );

                    // Có thể xóa form sau khi gửi
                    txtTieuDe.setText("");
                    txtNoiDung.setText("");
                }
            }
        });
    }

